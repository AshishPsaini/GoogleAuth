public class UserProfileModel {
	private String user_id;
	private String userName;
	private String userPicture;
	private String user_gender;
	private String user_google_link;
	private String user_locale;
	/**
	 * @return the user_id
	 */
	public String getUser_id() {
		return user_id;
	}
	/**
	 * @param user_id the user_id to set
	 */
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the userPicture
	 */
	public String getUserPicture() {
		return userPicture;
	}
	/**
	 * @param userPicture the userPicture to set
	 */
	public void setUserPicture(String userPicture) {
		this.userPicture = userPicture;
	}
	/**
	 * @return the user_gender
	 */
	public String getUser_gender() {
		return user_gender;
	}
	/**
	 * @param user_gender the user_gender to set
	 */
	public void setUser_gender(String user_gender) {
		this.user_gender = user_gender;
	}
	/**
	 * @return the user_google_link
	 */
	public String getUser_google_link() {
		return user_google_link;
	}
	/**
	 * @param user_google_link the user_google_link to set
	 */
	public void setUser_google_link(String user_google_link) {
		this.user_google_link = user_google_link;
	}
	/**
	 * @return the user_locale
	 */
	public String getUser_locale() {
		return user_locale;
	}
	/**
	 * @param user_locale the user_locale to set
	 */
	public void setUser_locale(String user_locale) {
		this.user_locale = user_locale;
	}

}
